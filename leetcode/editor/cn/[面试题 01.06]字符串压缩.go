func compressString(S string) string {
     length := len(S)
     if length <= 0 {
        return S
     }
     res := make([]byte, 0, length)
     cnt := 1
     cur := S[0]
     for i:=1;i<length;i++ {
        if cur == S[i] {
           cnt++
        } else{
           res = append(res, cur)
           res = append(res, []byte(strconv.Itoa(cnt))...)
           cnt = 1
           cur = S[i]
        }
     }
     res = append(res, cur)
     res = append(res, []byte(strconv.Itoa(cnt))...)
     if len(res) >= length {
        return S
     }
     return string(res)
}
